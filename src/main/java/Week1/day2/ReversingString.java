package Week1.day2;

public class ReversingString {

	public static void main(String[] args) {
		
		
		String S = "SRIVANI" ;
		
		char[] Ch = S.toCharArray();
		
	
		
		System.out.println(Ch.length);
		
		for (int i=Ch.length-1; i >= 0 ;i--)
		{
			

			
			System.out.println(Ch[i]);
		}
			
		

	}

}
